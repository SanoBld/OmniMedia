_LANGS: dict[str, dict[str, str]] = {"en": EN, "fr": FR}
_current: str = "en"


def set_language(lang: str) -> None:
    global _current
    _current = lang if lang in _LANGS else "en"


def current_language() -> str:
    return _current


def t(key: str, **kwargs) -> str:
    table = _LANGS.get(_current, EN)
    text  = table.get(key, EN.get(key, key))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text
